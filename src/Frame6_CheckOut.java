    private void ok_bActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ok_bActionPerformed
       String days =days_tf.getText();
        
        if (days.equals("")) {
            JOptionPane.showMessageDialog(null,"Please add the days staying in hotel");
            
        } else {
            checkOut CO = new checkOut();
        
            String roomType = roomType_tf.getText();
            CO.setRoomType(roomType);
            CO.setPrice();
            CO.setDays(Integer.parseInt(days));
            payment_tf.setText(String.valueOf(CO.calDays())); 
        }
    }//GEN-LAST:event_ok_bActionPerformed

    private void paid_bActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_paid_bActionPerformed
       String pay = payment_tf.getText();
        if (pay.equals("")) {
            JOptionPane.showMessageDialog(null,"Please enter the days staying in hotel before paying");
            
        } else {
            payment_tf.setForeground(Color.green);
            paidChecker++;
        }
    }//GEN-LAST:event_paid_bActionPerformed

